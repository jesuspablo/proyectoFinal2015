package net.daw.bean.generic.specific.implementation;

import net.daw.bean.generic.implementation.BeanGenImpl;
import net.daw.bean.publicinterface.BeanInterface;
import com.google.gson.annotations.Expose;

public class ProfesorBeanGenSpImpl extends BeanGenImpl implements BeanInterface {

    public ProfesorBeanGenSpImpl() {
    }
    @Expose
    private String nombre = "";
     @Expose
    private String email = "";
   
   @Expose
    private String primer_apellido = "";
   @Expose
    private String segundo_rapellido = "";
   @Expose
    private String especialista = "";
   
   
    public ProfesorBeanGenSpImpl(Integer id) {
        super(id);
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPrimer_apellido() {
        return primer_apellido;
    }

    public void setPrimer_apellido(String primer_apellido) {
        this.primer_apellido = primer_apellido;
    }

    public String getSegundo_rapellido() {
        return segundo_rapellido;
    }

    public void setSegundo_rapellido(String segundo_rapellido) {
        this.segundo_rapellido = segundo_rapellido;
    }

    public String getEspecialista() {
        return especialista;
    }

    public void setEspecialista(String especialista) {
        this.especialista = especialista;
    }

  

}
